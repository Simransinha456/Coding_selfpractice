#include <bits/stdc++.h>
using namespace std;

int main() {
    
    int n=4; // rows
    int m=4; // columns

    for(int i=0; i<n; i++){
        for(int j=0; j<m; j++){
            cout << j+1;
        }
        cout << endl;
    }
    return 0;
}




